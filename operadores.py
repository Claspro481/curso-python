#Operadores aritmeticos 
#SUMA
pokemones_ash = 5
pokemones_brok = 3
total_pokemones = pokemones_ash + pokemones_brok
print(f"Total pokemones: {total_pokemones}")

#Resta
velocidad_maxima_ferrari = 340
velocidad_maxima_tesla = 250
diferencia_velocidad = velocidad_maxima_ferrari - velocidad_maxima_tesla
print(f"Diferencia de velocidad: {diferencia_velocidad}")

#Multiplicacion 
precio_moto = 5000
cantidad_motos = 4
total_precios_motos = precio_moto * cantidad_motos
print(f"Costo total de motos: {total_precios_motos}")

#Division 
pokebolas = 120 
cantidad_entrenadores = 6
division_pokebolas = pokebolas / cantidad_entrenadores
print(f"Pokebolas por entrenador: {division_pokebolas}")

#Modulo 
cantidad_carros = 25
cantidad_filas = 4
carros_sobrantes =  cantidad_carros % cantidad_filas
print(f"Carros que sobran: {carros_sobrantes}")

#Potencia 
carga_auto = 2
elevacion = 10
unidades_de_energia = carga_auto ** elevacion
print(f"Unidades de energia: {unidades_de_energia}")


#Operadores Logicos 

#AND
es_pikachu_electrico = True
es_picachu_amarillo = True
resultado = es_picachu_amarillo and es_pikachu_electrico
print(f"Pikachu es amarillo y electrico?: {resultado}")

#or
carro_es_rojo = True
carro_es_lamborgini = False
resultado_or = carro_es_rojo or carro_es_lamborgini
print(f"Ell carro es rojo o de marca lamborgini?: {resultado_or}")

#NOT
moto_es_negra = False
resultado_not = not moto_es_negra
print(f"Esta moto no es de color negro? {resultado_not}")


